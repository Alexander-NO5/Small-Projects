def add(a, b):
  result = a + b
  return result

def substract(a, b):
  result = a - b
  return result

def multiply(a, b):
  result = a * b
  return result

def divide(a, b):
  result = a / b
  return result

def exp(a, b):
  result = a ** b
  return result

print(add(4, 2))
print(substract(4, 2))
print(multiply(4, 2))
print(divide(4, 2))
print(exp(4, 2))
